public class IndeksTerakhir {
    public static void main(String[] args) {
        int angka[] = {1, 2, 3, 4, 5};

        int IndeksTerakhir = angka.length - 1;

        System.out.println("Indeks Terakhir: " + IndeksTerakhir);

        System.out.println("Nilai pada Indeks Terakhir: " + angka[IndeksTerakhir]);
    }
}
